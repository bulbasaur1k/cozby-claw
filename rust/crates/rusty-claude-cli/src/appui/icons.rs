//! Карта иконок приложения — единый набор моноширинных глифов (без эмоджи).
//! В терминале «иконки» — это символы Unicode; цвет задаётся отдельно темой.
//! Держим их в одном месте, чтобы оформление было согласованным.

/// Маленькая точка-индикатор состояния (цвет задаёт вызывающий).
pub const DOT: &str = "•";
/// Полая точка — неактивное/спящее.
pub const DOT_HOLLOW: &str = "◦";
/// Маркер активной вкладки.
pub const ACTIVE: &str = "▸";
/// Пин (закреплённая вкладка).
pub const PIN: &str = "★";
/// Маркер реплики пользователя.
pub const USER: &str = "▍";
/// Маркер вызова инструмента.
pub const TOOL: &str = "▹";
/// Ветка результата инструмента.
pub const BRANCH: &str = "└";
/// Ошибка / успех.
pub const ERROR: &str = "✗";
/// Аккуратный спиннер (брайль) — маленький и ровный по ширине.
pub const SPINNER: [&str; 10] = ["⠋", "⠙", "⠹", "⠸", "⠼", "⠴", "⠦", "⠧", "⠇", "⠏"];
